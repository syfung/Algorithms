#ifndef INSERTION_SORT_H
#define INSERTION_SORT_H

/**
 * A sort function implementing the insertion sort algorithm. This
 * function will modify the original array.
 *
 * @Param
 *   - in_array the input array
 *   - size size of the array
 *
 * @Return
 *   - Also return the pointer to the input array
 */
int *insertion_sort(int *in_array, int size);

#endif /* INSERTION_SORT_H */
